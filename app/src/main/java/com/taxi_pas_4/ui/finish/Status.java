package com.taxi_pas_4.ui.finish;

public class Status {
    private String response;

    public String getResponse() {
        return response;
    }

    public void setResponse(String response) {
        this.response = response;
    }

    @Override
    public String toString() {
        return "Status{" +
                "response='" + response + '\'' +
                '}';
    }
}


