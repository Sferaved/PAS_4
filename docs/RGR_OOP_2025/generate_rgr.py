def calc_task1(v: dict, k: float = K_DEFAULT):
    b, p, F, Ch = v["VRCHb"], v["VRCHp"], v["F"], v["Ch"]
    Zs, Pss, Vb, Pb, Iz = v["Zs"], v["Pss"], v["Vb"], v["Pb"], v["Iz"]
    delta = b - p
    denom = F - p
    uvp = delta / denom * Ch
    prod = 100 * uvp / (Ch - uvp)
    ezp = uvp * Zs * (1 + Pss / 100)
    ess = (Vb / Pb) * delta * Iz * k * Ch
    return dict(
        delta=delta,
        denom=denom,
        uvp=uvp,
        prod=prod,
        ezp=ezp,
        ess=ess,
        k=k,
    )


def setup_doc_styles(doc: Document):
    section = doc.sections[0]
    section.page_height = Mm(297)
    section.page_width = Mm(210)
    section.left_margin = Mm(30)
    section.right_margin = Mm(15)
    section.top_margin = Mm(20)
    section.bottom_margin = Mm(20)

    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(14)
    style._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
    pf = style.paragraph_format
    pf.line_spacing = 1.5
    pf.space_after = Pt(0)
    pf.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY


def add_centered(doc, text, bold=False, size=14):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.bold = bold
    run.font.name = "Times New Roman"
    run.font.size = Pt(size)
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
    return p


def add_heading(doc, text, level=1):
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        run.font.name = "Times New Roman"
        run.font.size = Pt(14 if level > 1 else 16)
        run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
    return h


def add_para(doc, text):
    p = doc.add_paragraph(text)
    p.paragraph_format.first_line_indent = Cm(1.25)
    return p


def plot_series(x, y, xlabel, ylabel, title, filename, highlight=None):
    plt.rcParams["font.family"] = "DejaVu Sans"
    fig, ax = plt.subplots(figsize=(8, 5))
    ax.plot(x, y, "o-", color="#1f4e79", linewidth=2, markersize=7)
    if highlight:
        hx, hy, label = highlight
        ax.scatter(hx, hy, color="#c00000", s=80, zorder=5, label=label)
        ax.legend()
    ax.set_xlabel(xlabel, fontsize=11)
    ax.set_ylabel(ylabel, fontsize=11)
    ax.set_title(title, fontsize=12)
    ax.grid(True, alpha=0.35)
    fig.tight_layout()
    path = OUT_DIR / filename
    fig.savefig(path, dpi=150)
    plt.close(fig)
    return path


